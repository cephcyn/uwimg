void l1_normalize(image im)
{
    // 2.1 TODO
    // calculate image sum
    float sum = 0;
    for (int x = 0; x < im.w; x++) {
        for (int y = 0; y < im.h; y++) {
            for (int c = 0; c < im.c; c++) {
                sum += get_pixel(im, x, y, c);
            }
        }
    }
    // divide all pixels by sum
    for (int x = 0; x < im.w; x++) {
        for (int y = 0; y < im.h; y++) {
            for (int c = 0; c < im.c; c++) {
                set_pixel(im, x, y, c, get_pixel(im, x, y, c) / sum);
            }
        }
    }
}

